    @Column(name = "first_name")
    private String firstName;

    @Column(name = "last_name")
    private String lastName;

    @OneToOne
    @JoinColumn(name = "user_id", unique = true)
    private User user;

    // jhipster-needle-entity-add-field - JHipster will add fields here

